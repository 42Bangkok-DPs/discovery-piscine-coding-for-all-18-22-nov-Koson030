i = 0  # เริ่มจาก 0
while i <= 10:  # วนลูปแสดงสูตรคูณ 1 ถึง 10
    print(f"Table de {i}: ", end="")  # แสดงชื่อสูตรคูณ
    x = 0  # เริ่มคูณจาก 0
    while x <= 10:  # คูณจาก 0 ถึง 10
        print(f"{i * x} ", end="")  # แสดงผลคูณ
        x += 1  # เพิ่ม x
    print()  # ขึ้นบรรทัดใหม่
    i += 1  # เพิ่ม i